        <p><strong>¿Necesito registrarme para ver las promociones?</strong></p>
        <p>No. Cualquier usuario puede ver todas las promociones disponibles en los locales del shopping 
           sin necesidad de estar registrado. Sin embargo, para poder solicitar y utilizar un descuento 
           sí es necesario tener una cuenta activa.
        </p>

        <p><strong>¿Cómo me registro como cliente?</strong></p>
        <p>Podés registrarte desde la página de inicio completando el formulario con tu correo electrónico 
           y una contraseña. Una vez registrado, recibirás un correo con un enlace para validar tu cuenta. 
           Hasta que no valides tu cuenta, no podrás acceder a las promociones.
        </p>

        <p><strong>¿Cómo uso un descuento en un local?</strong></p>
        <p>Una vez registrado e iniciada la sesión, debés ingresar a la página del local 
           (que podés consultar en la sección de Locales), seleccionar la promoción que querés utilizar 
           y enviar la solicitud. El dueño del local será quien acepte o rechace dicha solicitud.
        </p>

        <p><strong>¿Qué categorías de cliente existen y cómo funcionan?</strong></p>
        <p>Existen tres categorías: <strong>Inicial</strong>, <strong>Medium</strong> y <strong>Premium</strong>. 
           Todo cliente comienza como Inicial al registrarse. A medida que utilizás más promociones en el shopping, 
           el sistema sube tu categoría automáticamente. Cuanto mayor sea tu categoría, más promociones 
           tenés disponibles para acceder.
        </p>


        <p><strong>¿Por qué no veo cierta promoción?</strong></p>
        <p>Las promociones tienen restricciones de vigencia, días de la semana habilitados y categoría mínima 
           de cliente requerida. Es posible que la promoción no esté activa en la fecha actual, 
           no corresponda al día de hoy, o sea exclusiva para clientes de categoría superior a la tuya.
        </p>

        <p><strong>¿Cómo me registro como dueño de local?</strong></p>
        <p>Podés registrarte completando el formulario correspondiente. Ten en cuenta que las cuentas de 
           dueños de locales deben ser aprobadas por el administrador del shopping antes de poder 
           acceder al sistema. Mientras tu cuenta esté pendiente de aprobación, verás un aviso indicándolo.
        </p>

       <p><strong>¿Cómo puedo contactar al administrador del shopping?</strong></p>
        <p>
            Podés enviarnos tu consulta a través del formulario de contacto disponible en la sección 
        <a href="/front/acercaDe.php">Acerca del shopping</a>. 
        Completá tu nombre, correo electrónico y mensaje, y nos comunicaremos a la brevedad.
        </p>

